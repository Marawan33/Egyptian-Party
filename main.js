var maxLength = 100;
$('textarea').keyup(function() {
  var length = $(this).val().length;
  var AmountLeft = maxLength-length;
  if(AmountLeft<=0)
            {
                 $("#chars").text("you have entered more than 100 character");
                
            }
        else{
        
        $("#chars").text(AmountLeft);
        }
});



